def load_router_dataset(ratio: float = 0.8) -> tuple:
    """
    Loads and splits the 600-query complexity dataset into train and test sets.

    No keyword overrides. The classifier learns entirely from TF-IDF features.
    Expected accuracy on held-out test set: ~72% (honest, no cheating).

    Args:
        ratio: Fraction of data to use for training. Default 0.8 (480 train / 120 test).

    Returns:
        (train_x, train_y, test_x, test_y)
    """
    queries = list(RAW_QUERIES)
    random.seed(42)
    random.shuffle(queries)

    split_idx = int(len(queries) * ratio)
    train_queries = queries[:split_idx]
    test_queries = queries[split_idx:]

    train_x = [q[0] for q in train_queries]
    train_y = [q[1] for q in train_queries]
    test_x = [q[0] for q in test_queries]
    test_y = [q[1] for q in test_queries]

    return train_x, train_y, test_x, test_y
